# Naming Convention

#Pascal Case
TilaMuhammad = "Hello, World!"
print(TilaMuhammad)

#Camel Case
tilaMuhammad = "Hello, World!"      
print(tilaMuhammad)

#Snake Case, Recommended   
tila_muhammad = "Hello, World!" 
print(tila_muhammad)

# Data Types in Python
# Numeric Types
integer_num = 10          # Integer
float_num = 10.5          # Float
complex_num = 1j          # Complex number
print(type(integer_num))
print(type(float_num))  
print(type(complex_num))        

# Boolean Type
is_true = True        # Boolean
is_false = False      # Boolean
print(type(is_true))
print(type(is_false))

# String Type
string_var = "Hello, Python!"   # String
print(type(string_var))

# Character Type
a = 'A'
print(ord(a))
print(chr(65))
